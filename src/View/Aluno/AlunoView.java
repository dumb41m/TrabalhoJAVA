    private void tb_alunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_alunosMouseClicked
        int linha = tb_alunos.getSelectedRow();
        String aoClicar = (tb_alunos.getModel().getValueAt(linha, 0).toString());

        try {
            String sql = "select * from alunos where id = '"+aoClicar+"'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if(rs.next()){
                String aluno_id = rs.getString("id");
                txt_id.setText(aluno_id);

                String aluno_nome = rs.getString("nome");
                txt_nome.setText(aluno_nome);

                //restauração do radio button de sexo selecionado
                String aluno_sexo = rs.getString("sexo");

                if("Masculino".equals(aluno_sexo)){
                    rb_masculino.setSelected(true);
                }

                if("Feminino".equals(aluno_sexo)){
                    rb_feminino.setSelected(true);
                }
                //fim do radio button de sexo selecionado

                String aluno_nascimento = rs.getString("dnascimento");
                txt_dnasc.setText(aluno_nascimento);

                String aluno_matricula = rs.getString("matricula");
                txt_matricula.setText(aluno_matricula);

                String aluno_curso = rs.getString("curso");
                txt_curso.setText(aluno_curso);

                String aluno_email = rs.getString("email");
                txt_email.setText(aluno_email);
            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
        
        bt_salvar.setEnabled(false);
        bt_editar.setEnabled(true);
        bt_excluir.setEnabled(true);
        bt_novo.setEnabled(true);
    }//GEN-LAST:event_tb_alunosMouseClicked

