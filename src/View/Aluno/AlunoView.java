    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
        aluno.setNome(txt_nome.getText());
        aluno.setSexo(sexo);
        aluno.setdNasc(txt_dnasc.getText());
        aluno.setMatricula(txt_matricula.getText());
        aluno.setCurso(txt_curso.getText());
        aluno.setEmail(txt_email.getText());

        //validações dos campos de inserção de informação
        //recebo os textos informados nos campos e jogo para variaveis
        String valida_nome = txt_nome.getText();
        String valida_dnasc = txt_dnasc.getText();
        String valida_matricula = txt_matricula.getText();
        String valida_curso = txt_curso.getText();
        String valida_semestre = txt_matricula.getText();
        String valida_email = txt_email.getText();

        if(valida_nome.equals("")){
            JOptionPane.showMessageDialog(null, "Insira o campo NOME");
        }else if( sexo == null){
            JOptionPane.showMessageDialog(null, "Selecione o SEXO - Masculino ou Feminino");
        }else if( valida_dnasc.equals("")){
            JOptionPane.showMessageDialog(null, "Selecione a DATA DE NASCIMENTO");
        }else if( valida_matricula.equals("")){
            JOptionPane.showMessageDialog(null, "Insira o campo MATRICULA");
        }else if( valida_curso.equals("")){
            JOptionPane.showMessageDialog(null, "Insira o nome do CURSO");
        }else if( valida_email.equals("")){
            JOptionPane.showMessageDialog(null, "Selecione o EMAIL");
        }else{//fim das validações dos campos de inserção de informação

            alunoBO.CadastrarAluno(aluno);
            LimpaTela();
            Atualizar();
        }
        
        bt_salvar.setEnabled(false);
        bt_editar.setEnabled(true);
        bt_excluir.setEnabled(true);
        bt_novo.setEnabled(true);
    }//GEN-LAST:event_bt_salvarActionPerformed

